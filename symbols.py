def plot_matrix(device, input_matrix):
	# assert 8x8
	
	for l_col in range(len(input_matrix)):
		for l_row in range(len(input_matrix[l_col])):
			device.pixel(l_row, l_col, input_matrix[l_col][l_row], redraw=False)
	
	device.flush()

